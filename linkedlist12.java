// sortedInsert method should return the head of the modified linked list.
class Solution {
    Node sortedInsert(Node head1, int key) {
        // Add your code here.
        Node item = new Node(key);
        if(key<head1.data)
        {
            item.next = head1;
            head1 = item;
        }
        else
        {
            Node prev = head1;
            Node temp = head1;
            while(temp!=null)
            {
                if(key<temp.data)
                {
                    prev.next = item;
                    item.next = temp;
                    break;
                }
                prev = temp;
                temp = temp.next;
            }
            prev.next = item;
        }
        return head1;
    }
}
